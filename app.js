function levenshtein(a, b) {
    const dp = Array.from({ length: a.length + 1 }, () => Array(b.length + 1).fill(0));
    for (let i = 0; i <= a.length; i++) {
        for (let j = 0; j <= b.length; j++) {
            if (i === 0) {
                dp[i][j] = j;
            }
            else if (j === 0) {
                dp[i][j] = i;
            }
            else if (a[i - 1] === b[j - 1]) {
                dp[i][j] = dp[i - 1][j - 1];
            }
            else {
                dp[i][j] = Math.min(dp[i - 1][j] + 1, dp[i][j - 1] + 1, dp[i - 1][j - 1] + 1);
            }
        }
    }
    return dp[a.length][b.length];
}
const distanciaMaximaNum = 1;
function mostrarResultados(busqueda) {
    const query = busqueda.toLowerCase();
    const distanciaMaxima = distanciaMaximaNum;
    const resultados = [];
    products.forEach((producto) => {
        const productoNombre = producto.name.toLowerCase();
        const distanciaProducto = levenshtein(query, productoNombre);
        if (productoNombre.includes(query) ||
            distanciaProducto <= distanciaMaxima) {
            resultados.push(producto.name);
        }
        else {
            const coincidencias = producto.synonyms.filter((sinonimo) => {
                const distanciaSinonimo = levenshtein(query, sinonimo.toLowerCase());
                return (sinonimo.toLowerCase().includes(query) ||
                    distanciaSinonimo <= distanciaMaxima);
            });
            if (coincidencias.length > 0) {
                resultados.push(producto.name);
            }
        }
    });
    if (resultados.length === 0) {
        return "";
    }
    return resultados[0];
}

